def arrecadacao():
    pequeno = int(input('digite a quantidade de camisetas pequenas'))

    medias = int(input('digite a quantidade de camisetas medias'))
    
    grandes = int(input('digite a quantidade de camisetas grandes'))
    
    total = (10*pequeno)+(12*medias)+(15*grandes)
    print('total arrecadado: '+str(total))
  
    


arrecadacao()
    